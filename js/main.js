function showPage(name){
  closeMobileNav();
  // Hide all
  document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
  // Show target
  const target = document.getElementById('page-' + name);
  if(target){
    target.classList.add('active');
    window.scrollTo({top: 0, behavior: 'instant'});
    // Show/hide shared inner footer
    const innerFt = document.getElementById('footer-inner');
    if(innerFt){
      // Show inner footer only for non-home, non-contact pages (contact has its own)
      innerFt.style.display = (name === 'home' || name === 'contact') ? 'none' : 'block';
      if(innerFt.style.display === 'block' && target.contains(innerFt) === false){
        target.appendChild(innerFt);
      }
    }
    // Re-trigger reveal observer for new content
    setTimeout(initReveals, 50);
    // Update nav state
    document.querySelectorAll('[data-page]').forEach(a => {
      a.classList.toggle('active', a.dataset.page === name);
    });
    // Init page-specific things
    
  }
}

